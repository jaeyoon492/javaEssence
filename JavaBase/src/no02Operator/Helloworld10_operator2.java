package no02Operator;

public class Helloworld10_operator2 {
	public static void main(String[] args) {
		
		int i=5 , j=5;
		
		System.out.println(i);//대입후
		i++;//증가
		
		++j;//증가후
		System.out.println(j);//대입
		
		System.out.println("i =" + i +",j =" + j);//증가가된i + 증가가되있던j
		
		
	}
}

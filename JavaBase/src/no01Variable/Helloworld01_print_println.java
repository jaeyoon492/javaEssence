package no01Variable;

public class Helloworld01_print_println {
		public static void main(String[] args) {   //main메서드의 선언부
		System.out.println("hello,world"); //화면에 글자를 출력한다.
		
		String str=("i want a take a shit");//실행될 문장들을 적는다.
		
		System.out.println(str);//문장을 출력한다.
		
		
		System.out.println("3+5");//""안에 들어가면 글자로 간주되어 계산되지 않고 그대로 출력된다.
		System.out.print(3+5);//print는 줄바꿈을 안하고 출력한다.
		System.out.println(8);//println은 줄바꿈을 하고 출력한다.
		
		System.out.println("Helloworld");//화면에 Hello,world를 출력하고 줄바꿈을 한다.
		System.out.print("Hello");//화면에 Hello를 출력하고 줄바꿈을 안한다.
		System.out.println("World");//화면에 World를 출력하고 줄바꿈을 한다.
		
		System.out.println("Hello world"); //화면에 Hello,world가 출력된다.
		System.out.print("3+5=");//화면에 3+5=을 출력하고 줄바꿈을 안한다.
		System.out.println(3+5);//화면에 8이 출력된다.
		
		System.out.println(5+3); //화면에 5+3의 결과값인 8이 출력된다.
		System.out.println(5-3); //화면에 5-3의 결과값인 2가 출력된다.
		System.out.println(5*3); //화면에 5*3의 결과값인 15가 출력된다.
		System.out.println(5/3); //화면에 5/3의 결과값인 1이 출력된다.
		
		
		
		
		
		
		
}
		
}
